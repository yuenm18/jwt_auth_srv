package com.my.model;

public class Login {
	private String user;
	private String password;
	public String getUser() {
		return user;
	}
	public void setUser(String username) {
		this.user = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
}
